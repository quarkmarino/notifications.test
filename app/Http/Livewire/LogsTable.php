    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('level_name')

           /** Example of custom column using a closure **/
            ->addColumn('level_name_lower', function (LogMessages $model) {
                return strtolower(e($model->level_name));
            })

            ->addColumn('level')
            ->addColumn('message')
            ->addColumn('context');
    }

